int	ft_strcmp(char *s1, char *s2);

int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	while ((s1[i] == s2[i]) && (s2[i] != '\0') && (s1[i] != '\0'))
		i++;
	return (s1[i] - s2[i]);
}
